package com.ibrhalil._0variables;

public class Main {
    public static void main(String[] args) {
        int a = 10, b = 20, c = 0;
        String name = "Ali";
        c = b + a;

        System.out.println("a: " + a);
        System.out.println("b: " + b);
        System.out.println("n: " + c);
        System.out.println("name: " + name);
        System.out.println();

        System.out.println("a + b + c: " + a + b + c);
        System.out.println("a + b + c: " + (a + b + c));
    }
}
