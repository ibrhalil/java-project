package com.ibrhalil._4._43;

public class Operator {
    public static void main(String[] args) {
        int a = 10;

        a++;
        a--;
        ++a;

        System.out.println("a : "+ a);
        System.out.println("a++ : "+ a++);
        System.out.println("a : "+ a);
        System.out.println("++a : "+ ++a);

    }
}
