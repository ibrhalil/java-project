package com.ibrhalil;

public class CarpmaIslemiKullanmadanCarpma {
    public static void main(String[] args) {
        int num1 = 23;
        int num2 = 12;
        int sum = 0;
        for (int i = 0; i < num2; i++) {
            sum += num1;
        }
        System.out.println(num1 + " * " + num2 + " = " + sum);
    }
}
