package com.ibrhalil._4._42;

public class Operator {
    public static void main(String[] args) {
        int num = -10;
        int t = -num;

        System.out.println("num : " + num);
        System.out.println("t : " + t);
        System.out.println();

        boolean gf = false;
        boolean gm = !gf;

        System.out.println("gf : " + gf);
        System.out.println("gm : " + gm);
        System.out.println();

    }
}
